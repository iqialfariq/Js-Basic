let jmlhAngkot = 10;
let angkotBeroperasi = 6;
let noAngkot = 1;

for (noAngkot; noAngkot <= jmlhAngkot; noAngkot++) {
  if (noAngkot <= angkotBeroperasi) {
    console.log("Angkot No. " + noAngkot + " Beroperasi Dengan Baik");
  } else {
    console.log("Angkot No. " + noAngkot + " Sedang Tidak Beroperasi");
  }
}
